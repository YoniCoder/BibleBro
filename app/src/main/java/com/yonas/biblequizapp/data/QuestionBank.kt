    // ============================================
    // SHUFFLE FUNCTIONS
    // ============================================
    fun getRandomQuestionsForLevel(levelQuestions: List<Question>, count: Int = 10): List<Question> {
        // Shuffle questions
        val shuffled = levelQuestions.shuffled(Random(System.currentTimeMillis()))
        val selected = shuffled.take(count)
        // Shuffle options for each selected question
        return selected.map { q ->
            val shuffledOptions = q.options.shuffled(Random(System.currentTimeMillis()))
            val newCorrectIndex = shuffledOptions.indexOf(q.options[q.correctAnswerIndex])
            Question(
                question = q.question,
                options = shuffledOptions,
                correctAnswerIndex = newCorrectIndex
            )
        }
    }

